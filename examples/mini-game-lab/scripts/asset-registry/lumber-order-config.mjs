import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { toImportName } from './core.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const cwd = process.env.LUMBER_ORDER_ROOT
  ? path.resolve(process.env.LUMBER_ORDER_ROOT)
  : path.resolve(scriptDir, '../..');
const importedDir = path.resolve(cwd, 'src/assets/imported');
const generatedDir = path.resolve(cwd, 'src/assets/generated');

export const lumberOrderAssetRegistryConfig = {
  cwd,
  importedDir,
  generatedDir,
  manifestPath: path.join(generatedDir, 'model-registry.manifest.json'),
  registryPath: path.join(generatedDir, 'model-registry.generated.ts'),
  scenePath: path.resolve(cwd, 'src/config/scene.json'),
  rulesPath: path.resolve(cwd, 'src/config/scene-json-v2-rules.json'),
  supportedExtension: '.glb',
  commands: {
    register: 'npm run asset:register',
    unregister: 'npm run asset:unregister',
  },
  async loadRules() {
    return JSON.parse(await fs.readFile(this.rulesPath, 'utf8'));
  },
  relativeImportedPath(fileName) {
    return `../imported/${fileName}`;
  },
  publicUrlForImportedAsset(fileName) {
    return `/src/assets/imported/${fileName}`;
  },
  toImportName,
  generateRegistry(manifest) {
    const imports = manifest
      .map((entry) => `import ${entry.importName} from '${entry.relativePath}?url';`)
      .join('\n');
    const entries = manifest
      .map((entry) => `  ${JSON.stringify(entry.sourceId)}: ${entry.importName},`)
      .join('\n');

    return [
      '// This file is generated by scripts/register-asset.mjs.',
      '// Do not edit by hand.',
      '',
      imports,
      imports ? '' : '',
      'export const GENERATED_MODEL_URL_MAP = {',
      entries,
      '} as const satisfies Record<string, string>;',
      '',
    ].join('\n');
  },
};
